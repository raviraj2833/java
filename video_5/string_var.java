public class string_var {
    public static void main(String[] args) {
        String var="r";
        switch(var)
        {
            case "r":
            System.out.println("You are Ravi");
            break;
            case "w":
            System.out.println("you are witcher");
            break;
            default:
            System.out.println("invalid");
        }
    }
}
