public class operator{
    public static void main(String[]args){
        int a=9;
        int b=9;
        int sum=a+b;
        //arithmetic operator
        System.out.println(sum);
        // Assignment operator
        System.out.println(a==b);
        // comparison operator
        System.out.println(65>9);
        // logical operator
        System.out.println(65<4 || 65<90);
        //bitwise operator
        System.out.println(2&5);
    
    }
}
